package ap3_franciscoRosenilson.unidade1.gabarito_aula2.br.com.rosenilson.aula2;

public class TiposPrimitivos {
 public static void main(String[] args) {
    byte codigoByte = 112;
    System.out.println(" ");
    System.out.println(codigoByte);
    
    short codigoShort = 112;
    System.out.println(" ");
    System.out.println(codigoShort);
    
    int codigoInt = 112;
    System.out.println(" ");
    System.out.println(codigoInt);
    
    long codigoLong = 112;
    System.out.println(" ");
    System.out.println(codigoLong);
    
    float codigoFloat = (float) 11.2;
    System.out.println(" ");
    System.out.println(codigoFloat);
    
    double codigoDouble = 112;
    System.out.println(" ");
    System.out.println(codigoDouble);
    
    boolean umaBooleana = false;
    System.out.println(" ");
    System.out.println(umaBooleana);
    
    char codigoChar = 'F';
    System.out.println(" ");
    System.out.println(codigoChar);

    String codigoString = "123";
    System.out.println(" ");
    System.out.println(codigoString);
 }   
}
